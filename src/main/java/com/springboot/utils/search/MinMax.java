package com.springboot.utils.search;

public class MinMax {

	private Double min;
	private Double max;
	
	public MinMax() {
		super();
	}
	
	public Double getMin() {
		return min;
	}
	public void setMin(Double min) {
		this.min = min;
	}
	public Double getMax() {
		return max;
	}
	public void setMax(Double max) {
		this.max = max;
	}
}
